        $('#case_type').val($(this).data('case_type'));
        $('#editModal').modal('show');
    });

    // Submit Edit Form
    $('#editForm').submit(function (e) {
        e.preventDefault();
        $.post('edit_clients.php', $(this).serialize(), function (response) {
            alert(response);
            location.reload(); // Refresh to see updated data
        });
    });

    // Delete Button
    $('.delete-btn').click(function () {
        if (confirm('Are you sure you want to delete this client?')) {
            $.post('delete_clients.php', { Client_ID: $(this).data('id') }, function (response) {
                alert(response);
                location.reload(); // Refresh to remove deleted row
            });
        }
    });
});
